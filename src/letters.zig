pub fn charToBigAsciiArt(letter: u8) ![]const u8 {
    if (letter < 65) {
        if (letter == 32) return _Space;
        return error.InvalidAsciiLetter;
    }
    if (letter > 122) {
        return error.InvalidAsciiLetter;
    }
    if (letter > 90 and letter < 97) {
        return error.InvalidAsciiLetter;
    }

    const arr_of_big_ascii = [_][]const u8{
        _A, _B, _C, _D, _E, _F, _G, _H, _I, _J, _K, _L, _M, _N, _O, _P, _Q, _R, _S, _T, _U, _V, _W, _X, _Y, _Z,
    };

    if (letter <= 90) {
        return arr_of_big_ascii[letter - 65];
    }
    return arr_of_big_ascii[letter - 97];
}
